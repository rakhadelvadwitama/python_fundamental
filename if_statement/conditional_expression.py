x = 10
y = 20
c = 10

z = x > y
print(z)  # Output: False, karena 10 tidak lebih besar dari 20

z = x < y
print(z)  # Output: True, karena 10 lebih kecil dari 20

z = x >= c
print(z)  # Output: True, karena 10 lebih besar atau sama dengan 10

z = x <= c
print(z)  # Output: True, karena 10 lebih kecil atau sama dengan 10

z = x == c
print(z)  # Output: True, karena 10 sama dengan 10

z = x != c
print(z)  # Output: False, karena 10 tidak berbeda dengan 10

