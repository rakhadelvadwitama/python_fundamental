# operator or yaitu mengembalikan True jika salah satu operandnya True, dan akan false jika kedua operandnya False.

# contoh operator or di dalam if statement dalam kehidupan sehari-hari adalah
# 1. seseorang bisa masuk ke dalam bioskop jika dia sudah berusia 17 tahun atau sudah menikah

buy_ticket = False
committed = True

if buy_ticket or committed:
    print("Anda boleh masuk bioskop")
else:
    print("Anda tidak boleh masuk bioskop")
